public class Q6 {
    public static void main(String[] args) {
        // Define the length of the rectangle (in meters)
        int length = 10;

        // Define the width of the rectangle (in meters)
        int width = 5;

        /* 
         * Calculate the area using the formula:
         * Area = length × width
         * The result will be in square meters (m²)
         */
        int area = length * width;

        // Display the calculated area
        System.out.println("Rectangle Area: " + area + " m²");
    }
}
