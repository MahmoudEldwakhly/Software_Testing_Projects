public class Lab8 {
    public static void main(String[] args) {
        // Use a for loop to print numbers from 0 to 9
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }

        // Print a message after the loop is finished
        System.out.println("counting Finished");
    }
}
